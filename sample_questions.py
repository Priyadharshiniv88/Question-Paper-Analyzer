def get_sample_questions(subject="OOP"):
    """Get sample questions for testing"""
    if subject.upper() == "OOP":
        return SAMPLE_OOP_QUESTIONS
    else:
        return SAMPLE_DS_QUESTIONS

def print_question_stats(questions):
    """Print statistics about the questions"""
    years = set(q['year'] for q in questions)
    parts = set(q['part'] for q in questions)
    topics = set(q['topic'] for q in questions)
    
    print(f"📊 Question Bank Statistics:")
    print(f"  • Total Questions: {len(questions)}")
    print(f"  • Years: {', '.join(sorted(years))}")
    print(f"  • Parts: {', '.join(sorted(parts))}")
    print(f"  • Topics: {len(topics)} unique topics")
    
    # Count by year
    print(f"\n📅 Questions by Year:")
    for year in sorted(years):
        count = sum(1 for q in questions if q['year'] == year)
        print(f"  • {year}: {count} questions")

